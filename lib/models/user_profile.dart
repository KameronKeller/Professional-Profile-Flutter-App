class UserProfile {
  final String name;
  final String email;
  final String github;
  final String phoneNumber;
  final String currentPosition;
  final String profilePhotoPath;

  const UserProfile({
    required this.name,
    required this.email,
    required this.github,
    required this.phoneNumber,
    required this.currentPosition,
    required this.profilePhotoPath,
  });
}
